  });

  AppState copywith({
    tryState,
    userDataState,
    addendumAdminState,
    ////////////////////// Rahi //////////////////////
    adminPendingTaskState,
    adminCurrentTaskState,
    adminSearchTaskState,
    adminAllTaskState,
    adminCompletedTaskState,
    adminAllVideoState,
   ////////////////////// Rahi //////////////////////
  }) {
    return AppState(
      tryState: tryState ?? this.tryState,
