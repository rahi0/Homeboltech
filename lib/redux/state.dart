  var tryState;
  var userDataState;
  var addendumAdminState;
  ////////////////////// Rahi //////////////////////
  var adminPendingTaskState;
  var adminCurrentTaskState;
  var adminSearchTaskState;
  var adminAllTaskState;
  var adminCompletedTaskState;
  var adminAllVideoState;
  ////////////////////// Rahi //////////////////////

  AppState({
    this.tryState,
    this.userDataState,
    this.addendumAdminState,
    ////////////////////// Rahi //////////////////////
    this.adminPendingTaskState,
    this.adminCurrentTaskState,
    this.adminSearchTaskState,
