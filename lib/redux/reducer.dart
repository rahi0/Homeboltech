import 'package:homeboltech/main.dart';
import 'package:homeboltech/redux/state.dart';

import 'action.dart';

AppState reducer(AppState state, dynamic action) {
  //////////////////////>>>>>>>>>>>> Admin <<<<<<<<<<<<<<//////////////////////
  ////////////////////// Amani //////////////////////

  if (action is TryAction) {
    print('hi from reducer if TryAction');
    return state.copywith(tryState: action.tryAction);
  } else if (action is UserDataAction) {
    return state.copywith(userDataState: action.userDataAction);
  } else if (action is AddendumAdminAction) {
    return state.copywith(addendumAdminState: action.addendumAdminAction);
  }

  ////////////////////// Amani //////////////////////


  ////////////////////// Rahi //////////////////////
  else if (action is AdminPendingTaskAction) {
    return state.copywith(adminPendingTaskState: action.adminPendingTaskAction);
  }

  else if (action is AdminCurrentTaskAction) {
    return state.copywith(adminCurrentTaskState: action.adminCurrentTaskAction);
  }

  else if (action is AdminSearchTaskAction) {
    return state.copywith(adminSearchTaskState: action.adminSearchTaskAction);
  }

  else if (action is AdminAllTaskAction) {
    return state.copywith(adminAllTaskState: action.adminAllTaskAction);
  }

  else if (action is AdminCompletedTaskAction) {
    return state.copywith(adminCompletedTaskState: action.adminCompletedTaskAction);
  }

  else if (action is AdminAllVideoAction) {
    return state.copywith(adminAllVideoState: action.adminAllVideoAction);
  }
  ////////////////////// Rahi //////////////////////


  ////////////////////// Nazmul //////////////////////
  ////////////////////// Nazmul //////////////////////


  ////////////////////// Sabiha //////////////////////
  ////////////////////// Sabiha //////////////////////


//////////////////////>>>>>>>>>>>> Admin <<<<<<<<<<<<<<//////////////////////



//////////////////////>>>>>>>>>>>> Agent <<<<<<<<<<<<<<//////////////////////

////////////////////// Amani //////////////////////
////////////////////// Amani //////////////////////


////////////////////// Rahi //////////////////////
////////////////////// Rahi //////////////////////


////////////////////// Nazmul //////////////////////
////////////////////// Nazmul //////////////////////


////////////////////// Sabiha //////////////////////
////////////////////// Sabiha //////////////////////

//////////////////////>>>>>>>>>>>> Agent <<<<<<<<<<<<<<//////////////////////



//////////////////////>>>>>>>>>>>> Client <<<<<<<<<<<<<<//////////////////////

////////////////////// Amani //////////////////////
////////////////////// Amani //////////////////////


////////////////////// Rahi //////////////////////
////////////////////// Rahi //////////////////////


////////////////////// Nazmul //////////////////////
////////////////////// Nazmul //////////////////////


////////////////////// Sabiha //////////////////////
////////////////////// Sabiha //////////////////////

//////////////////////>>>>>>>>>>>> Client <<<<<<<<<<<<<<//////////////////////



  return state;
}
